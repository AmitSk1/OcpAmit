package calculator;

import java.lang.Math;

public class Calculator {
    public int add(int num1, int num2) {
        return num1 + num2;
    }

    public int subtract(int num1, int num2) {
        return num1 - num2;
    }

    public double squareRoot(int num) {
        return Math.sqrt(num);
    }
}
